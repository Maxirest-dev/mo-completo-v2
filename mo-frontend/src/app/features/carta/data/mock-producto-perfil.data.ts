const DIAS = ['Lunes', 'Martes', 'Miercoles', 'Jueves', 'Viernes', 'Sabado', 'Domingo'];

function crearCalendario(allActive: boolean): CalendarioData[] {
  const canales = ['SALON', 'MOSTRADOR', 'DELIVERY', 'PEDIDOS YA'];
  return canales.map(canal => ({
    canal,
    dias: DIAS.map(dia => ({
      dia,
      turnos: [
        { activo: allActive },
        { activo: allActive },
        { activo: allActive },
      ],
    })),
  }));
}

function crearCalendarioSemana(): CalendarioData[] {
  const canales = ['SALON', 'MOSTRADOR', 'DELIVERY', 'PEDIDOS YA'];
  return canales.map(canal => ({
    canal,
    dias: DIAS.map((dia, i) => ({
      dia,
      turnos: [
        { activo: i < 5 },
        { activo: i < 6 },
        { activo: i < 5 },
      ],
    })),
  }));
}

